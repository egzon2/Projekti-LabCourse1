    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        try{
            int row = table.getSelectedRow();
            if(row == -1){
                Trajneri t = new Trajneri();
                //t.setTid(Integer.parseInt(TidTextField.getText()));
                t.setSid((Stafi)scmb.getSelectedItem());
                t.setEmri(emriTextField.getText());
                t.setMbiemri(mbiemriTextField.getText());
                t.setGjinia(gjiniaTextField.getText().charAt(0)); 
                t.setMosha(Integer.parseInt(moshaTextField.getText()));
                t.setNrTelefonit(Integer.parseInt(nrtelefonitTextField.getText())); 
                t.setQyteti(qytetiTextField.getText());
                t.setAdresa(adresaTextField.getText());
                tr.create(t);
            }else{
                Trajneri t = ttm.getTrajneri(row);
                //Id nuk e lejojm me ndryshu vetem emertimin ose fielda tjere qe mundeni me pas
                t.setSid((Stafi)scmb.getSelectedItem());
                t.setEmri(emriTextField.getText());
                t.setMbiemri(mbiemriTextField.getText());
                t.setGjinia(gjiniaTextField.getText().charAt(0));
                t.setMosha(Integer.parseInt(moshaTextField.getText()));
                t.setNrTelefonit(Integer.parseInt(nrtelefonitTextField.getText()));
                t.setQyteti(qytetiTextField.getText());
                t.setAdresa(adresaTextField.getText());
                tr.edit(t);
            }
            //E krijojm ni metode per me i clear fieldat mbasi ti shtojme
            clear();
            loadTable();
        }catch(FitnesiException ex){
            JOptionPane.showMessageDialog(this, "E dhena ekziston!");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if(row != -1){
            Object[] ob = {"Po","Jo"};
            int i = JOptionPane.showOptionDialog(this,"A deshironi ta fshini?","Fshirja", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if(i == 0){
                Trajneri t = ttm.getTrajneri(row);
                try{
                    tr.delete(t);
                }catch(FitnesiException ex){
                    JOptionPane.showMessageDialog(this,ex.getMessage());
                }
                clear();
                loadTable();
            }else{
                clear();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire!");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

