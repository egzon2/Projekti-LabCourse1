    public void loadTable(){
        try{
            List<Trajneri> lista = tr.findALL();
            ttm.addList(lista);
            table.setModel(ttm);
            ttm.fireTableDataChanged();
        }catch(FitnesiException ex){
            Logger.getLogger(FitnesiForm.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Trajneri t = ttm.getTrajneri(selectedIndex);
                    tidTextField.setText(t.getTid().toString());
                    scmb.setSelectedItem(t.getSid());
                    sidComboBox.repaint();
                    emriTextField.setText(t.getEmri());
                    mbiemriTextField.setText(t.getMbiemri());
                    gjiniaTextField.setText(t.getGjinia().toString());
                    moshaTextField.setText(t.getMosha().toString());
                    nrtelefonitTextField.setText(t.getNrTelefonit().toString()); 
                    qytetiTextField.setText(t.getQyteti());
                    adresaTextField.setText(t.getAdresa());
                }
            }
        });
    }
    
    public void loadComboBox(){
        try{
            List<Stafi> lista = ssr.findALL();
            scmb.add(lista);
            sidComboBox.setModel(scmb);
            sidComboBox.repaint();
        }catch(FitnesiException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

