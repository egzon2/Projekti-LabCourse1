    public void loadTable(){
        try{
            List<Stafi> lista = sr.findALL();
            stm.addList(lista);
            table.setModel(stm);
            stm.fireTableDataChanged();
        }catch(FitnesiException ex){
            Logger.getLogger(FitnesiForm.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Stafi s = stm.getStafi(selectedIndex);
                    sidTextField.setText(s.getSid().toString());
                    fcmb.setSelectedItem(s.getFid());
                    fitnesiCMB.repaint();
                    emriTextField.setText(s.getEmri());
                    mbiemriTextField.setText(s.getMbiemri());
                    moshaTextField.setText(s.getMosha().toString());
                    nrpersonalTextField.setText(s.getNrPersonal().toString());
                    pershkrimiTextField.setText(s.getPershkrimi());
                }
            }
        });
    }
    
    public void loadComboBox(){
        try{
            List<Fitnesi> lista = ffr.findALL();
            fcmb.add(lista);
            fitnesiCMB.setModel(fcmb);
            fitnesiCMB.repaint();
        }catch(FitnesiException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
