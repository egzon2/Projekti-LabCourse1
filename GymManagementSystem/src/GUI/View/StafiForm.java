    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        try{
            int row = table.getSelectedRow();
            if(row == -1){
                Stafi s = new Stafi();
                //p.setId(Integer.parseInt(idField.getText()));
                s.setFid((Fitnesi)fcmb.getSelectedItem());
                s.setEmri(emriTextField.getText());
                s.setMbiemri(mbiemriTextField.getText());
                s.setMosha(Integer.parseInt(moshaTextField.getText()));
                s.setNrPersonal(Integer.parseInt(nrpersonalTextField.getText()));
                s.setPershkrimi(pershkrimiTextField.getText());
                sr.create(s);
            }else{
                Stafi s = stm.getStafi(row);
                //Id nuk e lejojm me ndryshu vetem emertimin ose fielda tjere qe mundeni me pas
                s.setFid((Fitnesi)fcmb.getSelectedItem());
                s.setEmri(emriTextField.getText());
                s.setMbiemri(mbiemriTextField.getText());
                s.setMosha(Integer.parseInt(moshaTextField.getText()));
                s.setNrPersonal(Integer.parseInt(nrpersonalTextField.getText()));
                s.setPershkrimi(pershkrimiTextField.getText());
                sr.edit(s);
            }
            //E krijojm ni metode per me i clear fieldat mbasi ti shtojme
            clear();
            loadTable();
        }catch(FitnesiException ex){
            JOptionPane.showMessageDialog(this, "E dhena ekziston!");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

